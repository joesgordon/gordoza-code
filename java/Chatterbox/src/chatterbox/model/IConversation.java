package chatterbox.model;

import java.util.List;

import org.jutils.ui.event.ItemActionListener;

import chatterbox.data.ChatUser;
import chatterbox.messenger.Chat;

/*******************************************************************************
 * 
 ******************************************************************************/
public interface IConversation
{
    /***************************************************************************
     * Returns the name of this conversation.
     **************************************************************************/
    public String getConversationId();

    /***************************************************************************
     * Sends the provided message.
     * @param message
     **************************************************************************/
    public void sendMessage( ChatMessage message );

    /***************************************************************************
     * @param message
     **************************************************************************/
    public void receiveMessage( ChatMessage message );

    /***************************************************************************
     * 
     **************************************************************************/
    public void leaveConversation();

    /***************************************************************************
     * @return
     **************************************************************************/
    public Chat getChat();

    /***************************************************************************
     * @return
     **************************************************************************/
    public List<ChatUser> getUsers();

    /***************************************************************************
     * @param user
     **************************************************************************/
    public void addUser( ChatUser user );

    /***************************************************************************
     * @param user
     **************************************************************************/
    public void removeUser( ChatUser user );

    /***************************************************************************
     * @param user
     * @param available
     **************************************************************************/
    public void setUserAvailable( ChatUser user, boolean available );

    /***************************************************************************
     * @param user
     * @return
     **************************************************************************/
    public boolean isUserParticipating( ChatUser user );

    /***************************************************************************
     * @param l
     **************************************************************************/
    public void addMessageReceivedListener( ItemActionListener<ChatMessage> l );

    /***************************************************************************
     * @param l
     **************************************************************************/
    public void addUserListener( IUserListener l );
}
