package org.jutils.chart.data;

/*******************************************************************************
 * 
 ******************************************************************************/
public class ChartContext
{
    /**  */
    public double xMin;
    /**  */
    public double xMax;
    /**  */
    public double yMin;
    /**  */
    public double yMax;
    /**  */
    public int width;
    /**  */
    public int height;

    /***************************************************************************
     * 
     **************************************************************************/
    public ChartContext()
    {
        xMin = -5.0;
        xMax = 5.0;
        yMin = -5.0;
        yMax = 5.0;

        width = 0;
        height = 0;
    }

    /***************************************************************************
     * @return
     **************************************************************************/
    public double getXRange()
    {
        return xMax - xMin;
    }

    /***************************************************************************
     * @return
     **************************************************************************/
    public double getYRange()
    {
        return yMax - yMin;
    }
}
