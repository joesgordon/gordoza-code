package nmrc.data;

import nmrc.model.*;

/*******************************************************************************
 * 
 ******************************************************************************/
public class AminoAcid implements IAminoAcid
{
    /**  */
    private IShiftxRecord shiftxRecord;
    /**  */
    private IPeak peak;

    /***************************************************************************
     * @param record
     **************************************************************************/
    public AminoAcid( IShiftxRecord record )
    {
        shiftxRecord = record;
        peak = null;
    }

    /***************************************************************************
     *
     **************************************************************************/
    @Override
    public IPeak getPeak()
    {
        return peak;
    }

    /***************************************************************************
     *
     **************************************************************************/
    @Override
    public IShiftxRecord getShiftX()
    {
        return shiftxRecord;
    }

    /***************************************************************************
     *
     **************************************************************************/
    @Override
    public void setPeak( IPeak peak )
    {
        this.peak = peak;
    }
}
