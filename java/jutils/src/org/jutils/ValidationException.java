package org.jutils;

/*******************************************************************************
 * 
 ******************************************************************************/
public class ValidationException extends Exception
{
    /***************************************************************************
     * @param string
     **************************************************************************/
    public ValidationException( String string )
    {
        super( string );
    }

    /***************************************************************************
     * @param string
     * @param cause
     **************************************************************************/
    public ValidationException( String string, Throwable cause )
    {
        super( string, cause );
    }
}
