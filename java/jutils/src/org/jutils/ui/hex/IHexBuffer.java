package org.jutils.ui.hex;

/*******************************************************************************
 * 
 ******************************************************************************/
public interface IHexBuffer
{
    /***************************************************************************
     * @return
     **************************************************************************/
    public IHexBuffer getBuffer();

    /***************************************************************************
     * @param offset
     * @param size
     * @return
     **************************************************************************/
    public IHexBuffer getBuffer( long offset, long size );

    /***************************************************************************
     * @return
     **************************************************************************/
    public long getLength();

    /***************************************************************************
     * @param offset
     **************************************************************************/
    public void insertBytes( long offset );

    /***************************************************************************
     * @param bytes
     * @param offset
     **************************************************************************/
    public void insertBytes( byte[] bytes, long offset );

    /***************************************************************************
     * @param offset
     * @param length
     **************************************************************************/
    public void removeBytes( long offset, long length );
}
