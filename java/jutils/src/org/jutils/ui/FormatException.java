package org.jutils.ui;

/*******************************************************************************
 * 
 ******************************************************************************/
public class FormatException extends Exception
{
    /***************************************************************************
     * @param message
     **************************************************************************/
    public FormatException( String message )
    {
        super( message );
    }

    /***************************************************************************
     * @param message
     * @param cause
     **************************************************************************/
    public FormatException( String message, Throwable cause )
    {
        super( message, cause );
    }
}
