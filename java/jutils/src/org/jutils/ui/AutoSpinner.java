package org.jutils.ui;

import javax.swing.JSpinner;
import javax.swing.SpinnerModel;

/*******************************************************************************
 *
 ******************************************************************************/
public class AutoSpinner extends JSpinner
{
    /***************************************************************************
	 * 
	 **************************************************************************/
    public AutoSpinner()
    {
        super();
        init();
    }

    /***************************************************************************
     * @param model
     **************************************************************************/
    public AutoSpinner( SpinnerModel model )
    {
        super( model );
        init();
    }

    /***************************************************************************
     * 
     **************************************************************************/
    private void init()
    {
        this.addMouseWheelListener( new SpinnerWheelListener( this ) );
    }
}
