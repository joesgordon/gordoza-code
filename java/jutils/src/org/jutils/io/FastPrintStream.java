package org.jutils.io;

import java.io.*;

import org.jutils.Utils;

/*******************************************************************************
 * 
 ******************************************************************************/
public class FastPrintStream implements Closeable
{
    /**  */
    private final BufferedWriter writer;

    /***************************************************************************
     * @param file
     * @throws IOException
     **************************************************************************/
    public FastPrintStream( File file ) throws IOException
    {
        writer = new BufferedWriter( new FileWriter( file ), 64 * 1024 );
    }

    /***************************************************************************
     * 
     **************************************************************************/
    @Override
    public void close() throws IOException
    {
        writer.close();
    }

    /***************************************************************************
     * @param str
     **************************************************************************/
    public void print( String str )
    {
        write( str );
    }

    /***************************************************************************
     * 
     **************************************************************************/
    public void println()
    {
        writeNewLine();
    }

    /***************************************************************************
     * @param line
     **************************************************************************/
    public void println( String line )
    {
        write( line );
        writeNewLine();
    }

    /***************************************************************************
     * @param format
     * @param args
     **************************************************************************/
    public void println( String format, Object... args )
    {
        println( String.format( format, args ) );
    }

    /***************************************************************************
     * @param chars
     **************************************************************************/
    public void println( char [] chars )
    {
        write( chars );
    }

    /***************************************************************************
     * 
     **************************************************************************/
    private void writeNewLine()
    {
        write( Utils.NEW_LINE );
    }

    /***************************************************************************
     * @param str
     **************************************************************************/
    private void write( String str )
    {
        try
        {
            writer.write( str );
        }
        catch( IOException ex )
        {
            ex.printStackTrace();
        }
    }

    /***************************************************************************
     * @param chars
     **************************************************************************/
    private void write( char [] chars )
    {
        try
        {
            writer.write( chars );
        }
        catch( IOException ex )
        {
            ex.printStackTrace();
        }
    }
}
