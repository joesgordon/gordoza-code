package org.jutils.io;

import java.io.IOException;
import java.io.Writer;

/*******************************************************************************
 * @param <T>
 ******************************************************************************/
public interface IWriter<T>
{
    /***************************************************************************
     * @param item
     * @return
     * @throws IOException
     **************************************************************************/
    public void write( T item ) throws IOException;

    /***************************************************************************
     * @return
     **************************************************************************/
    public Writer getStream();
}
