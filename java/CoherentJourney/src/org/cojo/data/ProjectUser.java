package org.cojo.data;

/*******************************************************************************
 *
 ******************************************************************************/
public class ProjectUser
{
    /**  */
    public static final int INVALID_ID = -1;

    /**  */
    public final int id;
    /**  */
    public final User user;

    /***************************************************************************
     * @param id
     * @param userId
     * @param name
     **************************************************************************/
    public ProjectUser( int id )
    {
        this.id = id;
        this.user = new User();
    }

    /***************************************************************************
     * @return
     **************************************************************************/
    public String getName()
    {
        return user.getName();
    }
}
